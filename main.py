import pandas as pd
import numpy as np

# Создание DataFrame с данными
data = {
    'Имя': ['Алексей', 'Мария', 'Иван', 'Ольга', 'Дмитрий', 'Елена', 'Николай', 'Анна', 'Павел', 'Светлана'],
    'Математика': [85, 90, 78, 92, 88, 76, 84, 95, 89, 91],
    'Физика': [80, 85, 79, 88, 84, 75, 83, 90, 86, 87],
    'Химия': [78, 82, 77, 85, 83, 74, 81, 89, 84, 86],
    'Биология': [82, 87, 80, 90, 85, 78, 83, 91, 88, 89],
    'История': [75, 80, 74, 83, 81, 70, 79, 85, 82, 84]
}

df = pd.DataFrame(data)

# Вывод первых нескольких строк DataFrame
print("Первые несколько строк DataFrame:")
print(df.head())

# Вычисление средней оценки по каждому предмету
mean_scores = df.mean(numeric_only=True)
print("\nСредняя оценка по каждому предмету:")
print(mean_scores)

# Вычисление медианной оценки по каждому предмету
median_scores = df.median(numeric_only=True)
print("\nМедианная оценка по каждому предмету:")
print(median_scores)

# Вычисление Q1 и Q3 для оценок по математике
Q1_math = df['Математика'].quantile(0.25)
Q3_math = df['Математика'].quantile(0.75)
print(f"\nQ1 (25-й перцентиль) для математических оценок: {Q1_math}")
print(f"Q3 (75-й перцентиль) для математических оценок: {Q3_math}")

# Вычисление стандартного отклонения
std_deviation = df.std(numeric_only=True)
print("\nСтандартное отклонение по каждому предмету:")
print(std_deviation)